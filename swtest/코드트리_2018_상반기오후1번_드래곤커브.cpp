// 코드트리 2018 상반기 오후 1번 - 드래곤 커브
//
// 문제 요약
// 드래곤 커브를 N개 그린 뒤,
// 꼭짓점 4개가 모두 드래곤 커브 위에 있는
// 1x1 정사각형의 개수를 구하는 문제이다.
//
// 입력으로 하나의 드래곤 커브마다
// 시작점 (r, c), 시작 방향 d, 세대 g 가 주어진다.
//
// 드래곤 커브 규칙
// - 0세대: 시작 방향으로 1칸 이동하는 선분
// - 다음 세대는
//   이전 세대의 방향열을 뒤에서부터 보면서
//   각 방향에 +1(mod 4) 한 방향들을 뒤에 이어 붙인다.
//
// 예)
// 0세대 방향열: [0]
// 1세대: [0, 1]
// 2세대: [0, 1, 2, 1]
// 3세대: [0, 1, 2, 1, 2, 3, 2, 1]
//
// 핵심 아이디어
// 1. 각 드래곤 커브의 "이동 방향 배열"을 만든다.
// 2. 시작점부터 방향대로 실제 좌표를 이동하며 MAP에 표시한다.
// 3. 모든 커브를 다 그린 뒤,
//    1x1 정사각형의 네 꼭짓점이 모두 표시된 개수를 센다.

#include <stdio.h>

#define MAX (100 + 20)

int T;

int N;                         // 드래곤 커브 개수
int MAP[MAX][MAX];             // 해당 좌표가 드래곤 커브 위 점인지 표시
int dragonPosition[2000];      // 방향열 저장 배열 (2^10 = 1024보다 크게 확보)

// 방향 번호
// 0: 오른쪽, 1: 위쪽, 2: 왼쪽, 3: 아래쪽
int dr[] = { 0, -1, 0, 1 };
int dc[] = { 1,  0,-1, 0 };

// ---------------------------
// 드래곤 커브를 실제로 만들고 MAP에 표시하는 함수
// ---------------------------
void makeDragonCurve()
{
	scanf("%d", &N);

	// 드래곤 커브를 하나씩 처리
	for (int i = 0; i < N; i++)
	{
		int r, c, d, g;   // 시작점 (r, c), 시작 방향 d, 세대 g
		// 문제 원문에 따라 x, y라고 표현될 수 있으나
		// 이 코드에서는 r = 행, c = 열처럼 사용
		scanf("%d %d %d %d", &r, &c, &d, &g);

		// 0세대의 방향열은 시작 방향 하나뿐
		dragonPosition[0] = d;

		int length = 1;   // 현재 만들어진 방향열 길이

		// 1세대부터 g세대까지 방향열 확장
		for (int step = 1; step <= g; step++)
		{
			// 새로 붙는 부분의 길이는 기존 length와 같다
			//
			// 규칙:
			// 기존 방향열을 뒤에서부터 보며
			// 각 방향에 +1(mod 4) 한 값을 뒤에 붙인다.
			//
			// 예:
			// 기존: [0, 1]
			// 뒤에서부터: [1, 0]
			// +1 mod 4: [2, 1]
			// 결과: [0, 1, 2, 1]
			for (int i = length + 1; i <= length * 2; i++)
			{
				dragonPosition[i - 1] = (dragonPosition[length * 2 - i] + 1) % 4;
			}

			// 디버그용 출력
			// for (int i = 0; i < length * 2; i++)
			//     printf("%d, ", dragonPosition[i]);
			// putchar('\n');

			// 방향열 길이 2배 증가
			length *= 2;
		}

		// 시작점도 커브 위의 점이므로 표시
		MAP[r][c] = 1;

		// g세대의 총 이동 횟수는 2^g
		int moveCount = 1 << g;

		// 방향열을 따라 실제 좌표 이동하며 점 표시
		for (int k = 0; k < moveCount; k++)
		{
			r = r + dr[dragonPosition[k]];
			c = c + dc[dragonPosition[k]];

			MAP[r][c] = 1;
		}
	}
}

// ---------------------------
// 1x1 정사각형 개수 세기
//
// 네 꼭짓점:
// (r, c), (r+1, c), (r, c+1), (r+1, c+1)
//
// 이 네 점이 모두 드래곤 커브 위에 있으면 카운트
// ---------------------------
int countSquare()
{
	int sum = 0;

	for (int r = 0; r < 100; r++)
	{
		for (int c = 0; c < 100; c++)
		{
			// 네 꼭짓점이 모두 1이면 합이 4
			if (MAP[r][c] + MAP[r + 1][c] + MAP[r][c + 1] + MAP[r + 1][c + 1] == 4)
				sum++;
		}
	}

	return sum;
}

int main()
{
	// 원래 테스트 케이스 형식이 있을 수도 있지만
	// 현재 코드는 1회만 수행
	// scanf("%d", &T);
	T = 1;

	for (int tc = 1; tc <= T; tc++)
	{
		makeDragonCurve();

		printf("%d\n", countSquare());
	}

	return 0;
}