// BOJ 9012 - 괄호
// https://www.acmicpc.net/problem/9012
// 문제 요약
// 주어진 문자열이 올바른 괄호 문자열(VPS: Valid Parenthesis String)인지 판별한다.
//
// 올바른 괄호 문자열 조건
// 1. 여는 괄호 '(' 와 닫는 괄호 ')'의 개수가 같아야 한다.
// 2. 문자열을 왼쪽부터 보면서
//    어느 순간에도 닫는 괄호 ')'가 더 많아지면 안 된다.
//
// 해결 방법
// - 스택 대신 "현재 열린 괄호 개수"를 세는 방식으로 해결 가능
// - '(' 만나면 +1
// - ')' 만나면 -1
// - 중간에 음수가 되면 실패
// - 끝났을 때 0이면 성공

#include <stdio.h>
#include <stdbool.h>

int T;   // 테스트 케이스 수

int main()
{
	scanf("%d", &T);

	// T개의 문자열에 대해 반복
	for (int tc = 0; tc < T; tc++)
	{
		char str[50 + 5];   // 괄호 문자열 (최대 길이 + 여유)
		int sp;             // 현재 열린 괄호 개수 (stack pointer 느낌)
		int length;         // 문자열 길이
		bool flag;          // 중간에 오류 발생 여부

		scanf("%s", str);   // 문자열 입력

		sp = 0;             // 열린 괄호 개수 초기화
		flag = false;       // 오류 여부 초기화

		// ---------------------------
		// 문자열 길이 계산
		// ---------------------------
		// '\0' (문자열 끝)까지 세면서 길이 구함
		for (length = 0; str[length]; length++);

		// ---------------------------
		// 문자열 순회
		// ---------------------------
		for (int i = 0; i < length; i++)
		{
			int ch = str[i];   // 현재 문자

			// 여는 괄호면 +1
			if (ch == '(')
				sp++;
			else
				sp--;   // 닫는 괄호면 -1

			// ---------------------------
			// 중요 포인트
			// 닫는 괄호가 더 많아지는 순간 (sp < 0)
			// 이미 잘못된 문자열
			// ---------------------------
			if (sp < 0)
			{
				flag = true;   // 실패 표시
				break;         // 더 볼 필요 없음
			}
		}

		// ---------------------------
		// 최종 판별
		// ---------------------------
		// 1) 중간에 실패(flag == true)
		// 2) 끝났는데도 열린 괄호가 남아 있음 (sp != 0)
		// 둘 중 하나라도 해당되면 NO
		if (flag == true || sp != 0)
			printf("NO\n");
		else
			printf("YES\n");
	}

	return 0;
}