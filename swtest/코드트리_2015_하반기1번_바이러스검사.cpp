// 코드트리 2015 하반기 1번 - 바이러스 검사
//
// 문제 요약
// 각 식당마다 검사해야 할 인원 수가 주어진다.
// 각 식당에는
// 1. 팀장 검사관 1명은 반드시 배치해야 하고
// 2. 팀장이 검사하고 남은 인원이 있으면 팀원 검사관을 추가 배치한다.
//
// 검사 가능 인원
// - 팀장 1명: leader명 검사 가능
// - 팀원 1명: member명 검사 가능
//
// 목표
// 모든 식당을 검사하기 위해 필요한 총 검사관 수를 구하는 문제
//
// 핵심 아이디어
// 1. 모든 식당에는 팀장 1명이 무조건 필요하다.
// 2. 각 식당 인원 수에서 leader를 먼저 빼 준다.
// 3. 남은 인원이 0보다 크면 팀원을 추가한다.
// 4. 팀원 수는 "올림 나눗셈"으로 계산한다.
// 5. 합계가 커질 수 있으므로 long long 사용

#include <stdio.h>

#define MAX (1000000 + 50000)

typedef long long ll;

int T;

int N;                  // 식당 개수
int restaurant[MAX];    // 각 식당의 검사 대상 인원 수
int leader, member;     // 팀장 1명이 검사 가능한 인원, 팀원 1명이 검사 가능한 인원

// ---------------------------
// 입력 함수
// ---------------------------
void input()
{
	scanf("%d", &N);

	// 각 식당의 인원 수 입력
	for (int i = 0; i < N; i++)
		scanf("%d", &restaurant[i]);

	// 팀장, 팀원 검사 가능 인원 입력
	scanf("%d %d", &leader, &member);
}

int main()
{
	// 원래 테스트 케이스 형식일 수도 있지만
	// 현재 코드는 1번만 수행
	// scanf("%d", &T);
	T = 1;

	for (int tc = 1; tc <= T; tc++)
	{
		input();

		ll sum = 0;   // 필요한 총 검사관 수

		// ----------------------------------------
		// 1단계: 모든 식당에 팀장 1명씩 배치
		// ----------------------------------------
		// 팀장이 각 식당에서 leader명 검사하므로
		// 해당 인원만큼 먼저 빼 준다.
		for (int i = 0; i < N; i++)
			restaurant[i] -= leader;

		// 식당마다 팀장 1명은 무조건 필요하므로 N명 추가
		sum += N;

		// ----------------------------------------
		// 2단계: 남은 인원이 있으면 팀원 추가 배치
		// ----------------------------------------
		for (int i = 0; i < N; i++)
		{
			// 팀장이 다 못 검사해서 인원이 남은 경우
			if (restaurant[i] > 0)
			{
				// 필요한 팀원 수 =
				// ceil(restaurant[i] / member)
				//
				// 정수 연산으로 올림 나눗셈을 하면:
				// ((x - 1) / member) + 1
				sum += ((restaurant[i] - 1) / member) + 1;
			}
		}

		// 최종 검사관 수 출력
		printf("%lld\n", sum);
	}

	return 0;
}