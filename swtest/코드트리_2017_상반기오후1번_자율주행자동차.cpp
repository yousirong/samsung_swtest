// 코드트리 2017 상반기 오후 1번 - 자율주행자동차
// https://www.codetree.ai/training-field/frequent-problems/problems/autonomous-driving
// 문제 요약
// 자동차가 현재 위치와 방향을 가지고 있고,
// 정해진 규칙에 따라 도로를 탐색하면서 이동한다.
//
// 칸의 의미
// 0 : 도로(아직 방문 가능)
// 1 : 인도/벽(이동 불가)
// 2 : 이미 방문한 도로
//
// 방향 의미
// 0 : 북
// 1 : 동
// 2 : 남
// 3 : 서
//
// 핵심 규칙
// 1. 현재 칸은 방문 처리한다.
// 2. 현재 방향 기준 왼쪽 칸이 아직 방문하지 않은 도로면
//    왼쪽으로 회전한 뒤 그 칸으로 이동한다.
// 3. 왼쪽 칸이 갈 수 없으면,
//    왼쪽으로 회전만 하고 다시 검사한다.
//    이렇게 4방향을 모두 확인한다.
// 4. 4방향 모두 갈 수 없다면,
//    현재 바라보는 방향을 유지한 채 뒤로 한 칸 후진한다.
// 5. 뒤가 인도(벽)면 종료한다.
//
// 목표
// 자동차가 방문한 도로 칸의 개수를 출력한다.

#include <stdio.h>

#define MAX (50 + 5)

// 칸의 상태
#define ROAD   (0)   // 아직 방문하지 않은 도로
#define STREET (1)   // 인도/벽, 이동 불가
#define MARK   (2)   // 이미 방문한 도로

int T;
int N, M;     // 맵 크기
int R, C;     // 현재 자동차 위치
int D;        // 현재 방향
int MAP[MAX][MAX];

// 방향 배열: 북, 동, 남, 서
int dr[] = { -1, 0, 1, 0 };
int dc[] = { 0, 1, 0,-1 };

// ---------------------------
// 입력 함수
// ---------------------------
void input()
{
	scanf("%d %d %d %d %d", &N, &M, &R, &C, &D);

	for (int r = 0; r < N; r++)
		for (int c = 0; c < M; c++)
			scanf("%d", &MAP[r][c]);
}

// ---------------------------
// 디버그용 맵 출력
// ---------------------------
void printMap()
{
	for (int r = 0; r < N; r++)
	{
		for (int c = 0; c < M; c++)
			printf("%d ", MAP[r][c]);
		putchar('\n');
	}
	putchar('\n');
}

// ---------------------------
// 자동차 시뮬레이션
// ---------------------------
void simulate()
{
	// 현재 방향 D에서 "왼쪽 방향"으로 바꾸는 규칙
	//
	// D=0(북) -> 3(서)
	// D=1(동) -> 0(북)
	// D=2(남) -> 1(동)
	// D=3(서) -> 2(남)
	int changeDir[] = { 3, 0, 1, 2 };

	while (1)
	{
		// 1. 현재 위치 방문 처리
		MAP[R][C] = MARK;

		// 현재 방향 기준 왼쪽 방향 계산
		int nDir = changeDir[D];
		int nr = R + dr[nDir];
		int nc = C + dc[nDir];

		// ----------------------------------------
		// 경우 1) 왼쪽 칸이 아직 방문하지 않은 도로이면
		//         왼쪽으로 회전하고 그 칸으로 이동
		// ----------------------------------------
		if (MAP[nr][nc] == ROAD)
		{
			D = nDir;   // 방향을 왼쪽으로 바꿈
			R = nr;     // 위치 이동
			C = nc;
		}
		// ----------------------------------------
		// 경우 2) 왼쪽 칸이 인도이거나 이미 방문한 도로이면
		//         왼쪽으로 회전만 하면서 최대 4방향 확인
		// ----------------------------------------
		else
		{
			int i;

			for (i = 0; i < 4; i++)
			{
				// 실제로 방향을 왼쪽으로 회전
				D = nDir;

				// 새 방향 기준 앞칸 계산
				int nr = R + dr[D];
				int nc = C + dc[D];

				// 아직 방문하지 않은 도로가 있으면 그쪽으로 이동
				if (MAP[nr][nc] == ROAD)
				{
					R = nr;
					C = nc;
					break;
				}

				// 그 방향도 안 되면 다시 왼쪽으로 회전 준비
				nDir = changeDir[D];
			}

			// ----------------------------------------
			// 4방향 모두 갈 수 없는 경우
			// 뒤로 한 칸 후진
			// ----------------------------------------
			if (i == 4)
			{
				// 현재 방향 D의 반대 방향으로 한 칸 이동
				nr = R - dr[D];
				nc = C - dc[D];

				R = nr;
				C = nc;

				// 뒤가 인도/벽이면 종료
				if (MAP[R][C] == STREET)
					break;
			}
		}
	}
}

// ---------------------------
// 방문한 칸(MARK)의 개수 세기
// ---------------------------
int getAnswer()
{
	int sum = 0;

	for (int r = 0; r < N; r++)
		for (int c = 0; c < M; c++)
			if (MAP[r][c] == MARK)
				sum++;

	return sum;
}

int main()
{
	// 원래 테스트케이스 형식이 있을 수도 있지만
	// 현재 코드는 1회만 수행
	// scanf("%d", &T);
	T = 1;

	for (int tc = 1; tc <= T; tc++)
	{
		input();

		simulate();

		printf("%d\n", getAnswer());
	}

	return 0;
}