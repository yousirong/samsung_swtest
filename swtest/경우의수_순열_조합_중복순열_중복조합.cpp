// 경우의 수 기본 예제
//
// 1) 중복순열 (4 Pi 3)
//    - 1~4 중에서 3개를 순서를 고려해서 뽑음
//    - 같은 수를 여러 번 써도 됨
//    - 예: 1 1 1, 1 2 1 가능
//
// 2) 중복조합 (4 H 3)
//    - 1~4 중에서 3개를 순서 없이 뽑음
//    - 같은 수를 여러 번 써도 됨
//    - 예: 1 1 2 와 2 1 1 은 같은 경우로 취급
//
// 3) 조합 (4 C 3)
//    - 1~4 중에서 3개를 순서 없이 뽑음
//    - 같은 수를 중복해서 쓸 수 없음
//    - 예: 1 2 3 만 가능, 1 1 2 는 불가
//
// 4) 순열 (4 P 3)
//    - 1~4 중에서 3개를 순서를 고려해서 뽑음
//    - 같은 수를 중복해서 쓸 수 없음
//    - 예: 1 2 3 과 3 2 1 은 다른 경우

#include <stdio.h>
#include <stdbool.h>

int main()
{
	int count;   // 출력 번호를 세기 위한 변수

	// ----------------------------------------
	// 1. 중복순열 (4 Pi 3)
	// ----------------------------------------
	// 4개의 숫자 중에서 3개를 순서 있게 뽑는다.
	// 같은 숫자를 여러 번 선택할 수 있다.
	//
	// 각 자리에 1~4가 모두 들어갈 수 있으므로
	// 총 경우의 수 = 4 * 4 * 4 = 64
	if (1)
	{
		count = 1;

		for (int a = 1; a <= 4; a++)      // 첫 번째 자리
			for (int b = 1; b <= 4; b++)  // 두 번째 자리
				for (int c = 1; c <= 4; c++)  // 세 번째 자리
					printf("%d] %d %d %d\n", count++, a, b, c);
	}

	// ----------------------------------------
	// 2. 중복조합 (4 H 3)
	// ----------------------------------------
	// 4개의 숫자 중에서 3개를 순서 없이 뽑는다.
	// 같은 숫자를 여러 번 선택할 수 있다.
	//
	// 순서를 없애기 위해
	// a <= b <= c 형태로만 뽑는다.
	//
	// 예:
	// 1 1 2 는 출력되지만
	// 1 2 1, 2 1 1 은 같은 경우이므로 출력하지 않음
	if (0)
	{
		count = 1;

		for (int a = 1; a <= 4; a++)      // 첫 번째 수
			for (int b = a; b <= 4; b++)  // b는 a 이상
				for (int c = b; c <= 4; c++)  // c는 b 이상
					printf("%d] %d %d %d\n", count++, a, b, c);
	}

	// ----------------------------------------
	// 3. 조합 (4 C 3)
	// ----------------------------------------
	// 4개의 숫자 중에서 3개를 순서 없이 뽑는다.
	// 같은 숫자를 중복해서 사용할 수 없다.
	//
	// 순서를 없애기 위해
	// a < b < c 형태로만 뽑는다.
	//
	// 예:
	// 1 2 3 은 출력되지만
	// 3 2 1, 2 1 3 은 같은 경우라 출력하지 않음
	// 또한 1 1 2 같은 중복도 불가
	if (0)
	{
		count = 1;

		for (int a = 1; a <= 4; a++)          // 첫 번째 수
			for (int b = a + 1; b <= 4; b++)  // b는 a보다 커야 함
				for (int c = b + 1; c <= 4; c++)  // c는 b보다 커야 함
					printf("%d] %d %d %d\n", count++, a, b, c);
	}

	// ----------------------------------------
	// 4. 순열 (4 P 3)
	// ----------------------------------------
	// 4개의 숫자 중에서 3개를 순서 있게 뽑는다.
	// 같은 숫자를 중복해서 사용할 수 없다.
	//
	// 순서가 중요하므로
	// 1 2 3 과 3 2 1 은 다른 경우이다.
	//
	// 같은 숫자를 다시 쓰지 않기 위해 visit 배열 사용
	if (0)
	{
		count = 1;

		bool visit[5] = { 0 };  // 1~4 사용 여부 체크

		for (int a = 1; a <= 4; a++)
		{
			visit[a] = true;   // a 사용 처리

			for (int b = 1; b <= 4; b++)
			{
				// 이미 사용한 숫자는 건너뜀
				if (visit[b] == true) continue;

				visit[b] = true;  // b 사용 처리

				for (int c = 1; c <= 4; c++)
				{
					// 이미 사용한 숫자는 건너뜀
					if (visit[c] == true) continue;

					// a, b, c 모두 서로 다른 경우만 출력
					printf("%d] %d %d %d\n", count++, a, b, c);
				}

				visit[b] = false;  // b 사용 해제
			}

			visit[a] = false;  // a 사용 해제
		}
	}

	return 0;
}