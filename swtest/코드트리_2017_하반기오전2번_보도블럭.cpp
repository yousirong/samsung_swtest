// 코드트리 2017 하반기 오전 2번 - 보도블럭
//
// 문제 요약
// N x N 높이 지도가 주어지고,
// 길이 L의 경사로를 이용해 "지나갈 수 있는 길"의 개수를 구하는 문제이다.
//
// 길은
// - 모든 가로줄
// - 모든 세로줄
// 을 각각 하나의 길로 본다.
//
// 지나갈 수 있는 길의 조건
// 1. 인접한 칸의 높이 차이가 1보다 크면 불가능
// 2. 낮아지는 구간에서는 앞으로 L칸이 모두 같은 높이여야 경사로 설치 가능
// 3. 높아지는 구간에서도 반대쪽 기준으로 같은 조건을 만족해야 함
// 4. 이미 경사로를 놓은 칸 위에 또 경사로를 놓으면 안 됨
//
// 이 코드의 핵심 아이디어
// - 한 줄(row)을 "왼쪽 -> 오른쪽"으로 한 번 검사하며
//   낮아지는 구간(내리막) 경사로를 처리한다.
// - 그 줄을 뒤집은 inverse 배열을 만들어
//   다시 같은 방식으로 검사하면
//   원래 줄의 "높아지는 구간(오르막)"을 처리할 수 있다.
// - 즉, 한 방향 로직만 두 번 써서 양방향 조건을 모두 검사한다.

#include <stdio.h>
#include <stdbool.h>

#define MAX (100 + 20)

int T;
int N, L;              // N: 지도 크기, L: 경사로 길이
int MAP[MAX][MAX];     // 원본 지도
int TMAP[MAX][MAX];    // 전치 행렬 (세로줄을 가로줄처럼 검사하기 위한 배열)

// ---------------------------
// 입력 함수
// ---------------------------
void input()
{
	scanf("%d %d", &N, &L);

	for (int r = 1; r <= N; r++)
	{
		for (int c = 1; c <= N; c++)
		{
			scanf("%d", &MAP[r][c]);

			// TMAP[c][r] = MAP[r][c]
			// 전치 행렬을 미리 만들어 두면
			// 세로줄도 checkRow() 하나로 검사 가능
			TMAP[c][r] = MAP[r][c];
		}
	}
}

// ---------------------------
// 절댓값 함수
// ---------------------------
int abs(int x)
{
	return (x > 0) ? x : -x;
}

// ---------------------------
// arr[start] ~ arr[end] 구간이 모두 같은 높이인지 검사
//
// 경사로를 놓을 수 있으려면
// 해당 구간이 전부 같은 높이여야 한다.
// ---------------------------
bool isFlat(int arr[MAX], int start, int end)
{
	int value = arr[start];

	for (int i = start + 1; i <= end; i++)
	{
		if (value != arr[i])
			return false;
	}

	return true;
}

// ---------------------------
// 한 줄(arr)이 지나갈 수 있는 길인지 검사
//
// 반환값:
//   1 -> 가능
//   0 -> 불가능
//
// 동작 방식:
// 1. 원래 배열을 왼쪽 -> 오른쪽으로 보며 "내리막" 처리
// 2. 배열을 뒤집은 inverse를 다시 왼쪽 -> 오른쪽으로 보며
//    원래 배열의 "오르막" 처리
//
// visit[]         : 원래 방향에서 이미 경사로를 놓은 칸
// visit_inverse[] : 뒤집은 방향에서 이미 경사로를 놓은 칸
// ---------------------------
int checkRow(int arr[MAX])
{
	bool visit[MAX] = { 0 };          // 원래 배열 기준 경사로 설치 여부

	int inverse[MAX] = { 0 };         // 뒤집은 배열
	bool visit_inverse[MAX] = { 0 };  // 뒤집은 배열 기준 경사로 설치 여부

	// ----------------------------------------
	// 1단계: 원래 방향(왼쪽 -> 오른쪽) 검사
	// 여기서는 "높이가 낮아지는 경우"를 처리
	// ----------------------------------------
	for (int c = 1; c <= N - 1; c++)
	{
		// 높이가 같으면 문제 없음
		if (arr[c] == arr[c + 1])
			continue;

		// 조건 1) 높이 차이가 1보다 크면 불가능
		if (abs(arr[c] - arr[c + 1]) > 1)
			return 0;

		// 현재 칸이 다음 칸보다 높으면 "내리막"
		// 즉, 다음 낮은 칸들에 경사로를 놓아야 함
		if (arr[c] > arr[c + 1])
		{
			// 경사로 길이 L만큼 앞으로 갈 수 있어야 함
			if (c + L > N)
				return 0;

			// 그 구간이 전부 같은 높이여야 함
			if (isFlat(arr, c + 1, c + L) == false)
				return 0;

			// 그 칸들에 경사로 설치 표시
			for (int k = c + 1; k <= c + L; k++)
				visit[k] = true;
		}
	}

	// ----------------------------------------
	// 2단계: 뒤집은 배열 만들기
	// 원래 배열의 "오르막"을
	// 뒤집은 배열의 "내리막"처럼 처리하기 위함
	// ----------------------------------------
	for (int c = 1; c <= N; c++)
	{
		inverse[c] = arr[N + 1 - c];
		visit_inverse[c] = visit[N + 1 - c];
	}

	// ----------------------------------------
	// 3단계: 뒤집은 배열 검사
	// 여기서는 원래 배열 기준 "오르막" 처리
	// ----------------------------------------
	for (int c = 1; c <= N - 1; c++)
	{
		// 높이가 같으면 패스
		if (inverse[c] == inverse[c + 1])
			continue;

		// 조건 1) 높이 차이가 1보다 크면 불가능
		if (abs(inverse[c] - inverse[c + 1]) > 1)
			return 0;

		// 뒤집은 배열에서 내리막이면
		// 원래 배열에서는 오르막에 해당
		if (inverse[c] > inverse[c + 1])
		{
			// 경사로 길이 확보 가능해야 함
			if (c + L > N)
				return 0;

			// 낮은 칸들이 모두 같은 높이여야 함
			if (isFlat(inverse, c + 1, c + L) == false)
				return 0;

			// 조건 3) 이미 원래 방향에서 경사로를 놓았던 칸이면 중복 설치 불가
			for (int k = c + 1; k <= c + L; k++)
			{
				if (visit_inverse[k] == true)
					return 0;
			}
		}
	}

	// 모든 조건 통과
	return 1;
}

// ---------------------------
// 모든 가로줄 + 세로줄 검사
//
// MAP[r]  : r번째 가로줄
// TMAP[r] : 원래 배열의 r번째 세로줄
// ---------------------------
int checkAllRow()
{
	int sum = 0;

	for (int r = 1; r <= N; r++)
	{
		sum += checkRow(MAP[r]);   // 가로줄 검사
		sum += checkRow(TMAP[r]);  // 세로줄 검사
	}

	return sum;
}

int main()
{
	// 원래 테스트케이스 형식이 있을 수도 있지만
	// 현재 코드는 1회만 수행
	// scanf("%d", &T);
	T = 1;

	for (int tc = 1; tc <= T; tc++)
	{
		input();

		printf("%d\n", checkAllRow());
	}

	return 0;
}