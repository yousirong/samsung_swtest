// 코드트리 2019 하반기 오전 1번 - 종전
// https://www.codetree.ai/training-field/frequent-problems/problems/war-finish
// 문제 요약
// N x N 격자를 5개의 구역으로 나눈다.
// 기준점 (sr, sc)와 두 길이 d1, d2를 정하면
// 문제 조건에 따라 5개 구역이 결정된다.
//
// 각 구역의 인구 합을 구하고,
// (가장 큰 구역 인구 - 가장 작은 구역 인구)의 최솟값을 찾는 문제이다.
//
// 핵심 아이디어
// 1. 가능한 모든 (sr, sc, d1, d2)를 전부 시도한다.
// 2. calculate()에서 실제로 5개 구역을 나눈다.
// 3. 각 구역 인구 합을 구해서 최대-최소 차이를 계산한다.
// 4. 그 중 최소값을 정답으로 갱신한다.
//
// 이 코드는
// - 가운데 특수 구역을 먼저 1번 구역으로 채우고
// - 바깥쪽 네 영역을 2,3,4,5번 구역으로 채우는 방식이다.

#include <stdio.h>

#define MAX (20 + 5)
#define INF (0x7fff0000)

int T;

int N;
int MAP[MAX][MAX];   // 인구 정보
int minAnswer;       // 정답(최소 인구 차이)

// ---------------------------
// 입력 함수
// ---------------------------
void input()
{
	scanf("%d", &N);

	// 바깥 테두리를 -1로 채운다.
	// 이유:
	// d1, d2를 늘려 가면서 범위를 벗어나는지 확인할 때
	// 간단히 MAP[][] == -1 인지만 보면 되도록 하기 위함
	for (int r = 0; r <= N + 1; r++)
	{
		for (int c = 0; c <= N + 1; c++)
		{
			MAP[r][c] = -1;
		}
	}

	// 실제 인구 입력
	for (int r = 1; r <= N; r++)
	{
		for (int c = 1; c <= N; c++)
		{
			scanf("%d", &MAP[r][c]);
		}
	}
}

// ---------------------------
// 디버그용 출력 함수
// ---------------------------
void printMap(int map[MAX][MAX])
{
	for (int r = 1; r <= N; r++)
	{
		for (int c = 1; c <= N; c++)
			printf("%d ", map[r][c]);
		putchar('\n');
	}
	putchar('\n');
}

// --------------------------------------------------
// 하나의 기준점 (sr, sc), d1, d2가 정해졌을 때
// 실제 구역을 나누고 인구 차이를 계산하는 함수
//
// area[r][c] 의미:
// 1~5 : 각 구역 번호
// 0   : 아직 배정 안 됨
// --------------------------------------------------
void calculate(int sr, int sc, int d1, int d2)
{
	int area[MAX][MAX] = { 0 };  // 각 칸이 어느 구역인지 저장
	int sum[6] = { 0 };          // 각 구역 인구 합 (1~5만 사용)
	int min, max;
	int start, end, left, right;

	// ----------------------------------------
	// 1번 구역(가운데 특수 구역) 채우기
	//
	// 시작할 때는 row = sr 에서 start=end=sc
	// 즉, 첫 줄은 (sr, sc) 한 칸만 포함
	//
	// 아래로 한 줄씩 내려가면서
	// start와 end를 움직여 [start ~ end] 구간을 1번으로 칠한다.
	//
	// 처음에는
	// - 왼쪽 경계(start)는 왼쪽으로 퍼지고
	// - 오른쪽 경계(end)는 오른쪽으로 퍼진다.
	//
	// 이후 왼쪽 꼭짓점(sc-d1), 오른쪽 꼭짓점(sc+d2)에 도달하면
	// 다시 안쪽으로 좁혀진다.
	// ----------------------------------------
	start = end = sc;
	left = -1;
	right = 1;

	for (int r = sr; r <= sr + d1 + d2; r++)
	{
		// 현재 행의 [start ~ end]를 전부 1번 구역으로 채움
		for (int c = start; c <= end; c++)
			area[r][c] = 1;

		// 다음 행을 위해 경계 이동
		start += left;
		end += right;

		// 왼쪽 경계가 가장 왼쪽 꼭짓점에 닿으면 다시 오른쪽으로 이동
		if (start == sc - d1)
			left = 1;

		// 오른쪽 경계가 가장 오른쪽 꼭짓점에 닿으면 다시 왼쪽으로 이동
		if (end == sc + d2)
			right = -1;
	}

	// ----------------------------------------
	// 2번 구역
	//
	// 조건:
	// 1 <= r <= sr + d1 - 1
	// 1 <= c <= sc
	//
	// 단, 1번 구역을 만나면 그 행의 더 오른쪽은 볼 필요가 없으므로 break
	// ----------------------------------------
	for (int r = 1; r <= N; r++)
	{
		for (int c = 1; c <= N; c++)
		{
			if (area[r][c] == 1)
				break;

			if ((1 <= r && r <= sr + d1 - 1) &&
				(1 <= c && c <= sc))
			{
				area[r][c] = 2;
			}
		}
	}

	// ----------------------------------------
	// 3번 구역
	//
	// 조건:
	// 1 <= r <= sr + d2
	// sc + 1 <= c <= N
	//
	// 1번 구역은 건너뛰고 채움
	// ----------------------------------------
	for (int r = 1; r <= N; r++)
	{
		for (int c = 1; c <= N; c++)
		{
			if (area[r][c] == 1)
				continue;

			if ((1 <= r && r <= sr + d2) &&
				(sc + 1 <= c && c <= N))
			{
				area[r][c] = 3;
			}
		}
	}

	// ----------------------------------------
	// 4번 구역
	//
	// 조건:
	// sr + d1 <= r <= N
	// 1 <= c <= sc - d1 + d2 - 1
	//
	// 단, 그 행에서 1번 구역을 만나면 break
	// ----------------------------------------
	for (int r = 1; r <= N; r++)
	{
		for (int c = 1; c <= N; c++)
		{
			if (area[r][c] == 1)
				break;

			if ((sr + d1 <= r && r <= N) &&
				(1 <= c && c <= sc - d1 + d2 - 1))
			{
				area[r][c] = 4;
			}
		}
	}

	// ----------------------------------------
	// 5번 구역
	//
	// 조건:
	// sr + d2 + 1 <= r <= N
	// sc - d1 + d2 <= c <= N
	//
	// 1번 구역은 제외하고 채움
	// ----------------------------------------
	for (int r = 1; r <= N; r++)
	{
		for (int c = 1; c <= N; c++)
		{
			if (area[r][c] == 1)
				continue;

			if ((sr + d2 + 1 <= r && r <= N) &&
				(sc - d1 + d2 <= c && c <= N))
			{
				area[r][c] = 5;
			}
		}
	}

	// 디버그용
	// printMap(area);

	// ----------------------------------------
	// 각 구역 인구 합 계산
	// ----------------------------------------
	for (int r = 1; r <= N; r++)
	{
		for (int c = 1; c <= N; c++)
		{
			sum[area[r][c]] += MAP[r][c];
		}
	}

	// 최대/최소 인구 찾기
	min = INF;
	max = -INF;

	for (int i = 1; i <= 5; i++)
	{
		if (sum[i] < min) min = sum[i];
		if (sum[i] > max) max = sum[i];
	}

	// 현재 경우의 인구 차이로 정답 갱신
	if ((max - min) < minAnswer)
		minAnswer = max - min;
}

// --------------------------------------------------
// 가능한 모든 (sr, sc, d1, d2)를 시도하는 함수
//
// 기준점:
// - r은 최소 1, 최대 N-2
// - c는 최소 2, 최대 N-1
//
// d1, d2를 1부터 늘려 가면서
// 경계가 범위를 벗어나지 않는 경우만 calculate() 수행
// --------------------------------------------------
void simulate()
{
	for (int r = 1; r <= N - 2; r++)
	{
		for (int c = 2; c <= N - 1; c++)
		{
			int d1 = 1;

			while (1)
			{
				// 왼쪽 아래 방향 경계가 격자 밖이면 d1 종료
				if (MAP[r + d1][c - d1] == -1)
					break;

				int d2 = 1;

				while (1)
				{
					// 오른쪽 아래 방향 경계 또는
					// 가장 아래 꼭짓점이 격자 밖이면 d2 종료
					if (MAP[r + d2][c + d2] == -1 ||
						MAP[r + d1 + d2][c + d2 - d1] == -1)
						break;

					calculate(r, c, d1, d2);

					d2++;
				}

				d1++;
			}
		}
	}
}

int main()
{
	// scanf("%d", &T);
	T = 1;

	for (int tc = 1; tc <= T; tc++)
	{
		input();

		minAnswer = INF;

		simulate();

		printf("%d\n", minAnswer);
	}

	return 0;
}